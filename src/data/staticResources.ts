// Helper functions for demo data
export function getRandomDuration() {
  const durations = ['5 min', '10 min', '30 min', '1 hour', '2 hours', '1 week', 'Self-paced'];
  return durations[Math.floor(Math.random() * durations.length)];
}

export function getRandomRating() {
  return Number((4 + Math.random()).toFixed(1));
}

export function getRandomDifficulty(): 'Beginner' | 'Intermediate' | 'Advanced' {
  const levels = ['Beginner', 'Intermediate', 'Advanced'] as const;
  return levels[Math.floor(Math.random() * levels.length)];
}
